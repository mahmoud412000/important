#ifndef STD_TYPES_H
#define STD_TYPES_H

typedef unsigned char                           uint8;
typedef char                                    sint8;
typedef unsigned short int                      uint16;
typedef short int                               sint16;
typedef unsigned long int                       uint32;
typedef long int                                sint32;
typedef float                                   float32;
typedef double                                  float64;

#define NULL             ((void*)0)
#define true                 1
#define false                0
#define STD_HIGH             1
#define STD_LOW              0

#endif

